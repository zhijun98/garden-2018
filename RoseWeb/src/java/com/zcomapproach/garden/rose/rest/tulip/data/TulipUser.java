package com.zcomapproach.garden.rose.rest.tulip.data;

import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author zhijun98
 */
@XmlRootElement
public class TulipUser extends TulipData{

    private String uuid;
    private String username;
    private String password;
    private String mobileNumber;
    private String confirmCode;
    
    private String tulipVersion;
    
    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(String mobileNumber) {
        this.mobileNumber = mobileNumber;
    }

    public String getConfirmCode() {
        return confirmCode;
    }

    public void setConfirmCode(String confirmCode) {
        this.confirmCode = confirmCode;
    }

    public String getTulipVersion() {
        return tulipVersion;
    }

    public void setTulipVersion(String tulipVersion) {
        this.tulipVersion = tulipVersion;
    }
    
}
